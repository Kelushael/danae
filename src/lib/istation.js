export function detectMode(text) {
  const t = text.toLowerCase();
  if (t.match(/hygiene|clean|missing|blank|empty|field/))    return 'hygiene';
  if (t.match(/renew|renewal|merge|child|move/))              return 'renewal';
  if (t.match(/stuck|stall|no activity|inactive|dormant/))    return 'stuck';
  if (t.match(/order|cpq|sku|price|config|validation/))       return 'order';
  if (t.match(/summar|brief|execut|report|overview|health/))  return 'summary';
  if (t.match(/csv|upload|export|data|salesforce/))           return 'csv';
  if (t.match(/cost|spend|saving|budget|roi|waste/))          return 'cost';
  if (t.match(/team|adoption|workflow|cadence|prompt/))       return 'adoption';
  return 'general';
}

